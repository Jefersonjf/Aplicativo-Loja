
package models;

public class ClienteModel {
    public String nomeCompleto;
    public String data;
    public String cpf;
    public String telefone;
    public String celular;
    public String email;
    
    public ClienteModel(String nomeCompleto, String data, String cpf, String telefone, String celular, String email){
        this.nomeCompleto = nomeCompleto;
        this.data = data;
        this.cpf = cpf;
        this.telefone = telefone;
        this.celular = celular;
        this.email = email;
        
        
    }

  
    
}
